package Utilidades;

public class Etiquetas {
    public static final String RESET   = "\u001B[0m";

    public static final String INFO    = "\u001B[33m" + "[INFO] " + RESET;     /// Cyan
    public static final String ERROR   = "\u001B[31m" + "[ERROR] " + RESET;    /// Rojo/// Amarillo
    public static final String EXITO = "\u001B[32m" + "[EXITO] " + RESET;
    public static final String WARNING = "\u001B[33m" + "[WARNING] " + RESET;



}